package com.example.deezerapirecyclerview;

public class Album {
    private int id;
    private String title, cover, cover_small, cover_medium, cover_big, cover_xl, type;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getCover() {
        return cover;
    }

    public String getCover_small() {
        return cover_small;
    }

    public String getCover_medium() {
        return cover_medium;
    }

    public String getCover_big() {
        return cover_big;
    }

    public String getCover_xl() {
        return cover_xl;
    }

    public String getType() {
        return type;
    }
}
